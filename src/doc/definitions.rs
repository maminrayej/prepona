//! Contains definitions of phrases used across the crate and possibly links to other resources about the phrase.
//!
//! * `Time Complexity`: Describes the amount of computer time it takes to run an algorithm.
//!                      Time complexity is commonly estimated by counting the number of elementary operations performed by the algorithm,
//!                      supposing that each elementary operation takes a fixed amount of time to perform.
//!                      read more [here](https://en.wikipedia.org/wiki/Time_complexity).
//! * `Hyperedge`: A hyperedge is a non-empty subset of vertices. All vertices participating in a hyperedge are connected together.
//!                read more [here](https://en.wikipedia.org/wiki/Hypergraph).
//!
//! * `Directed Hyperedge`: A directed hyperedge is an ordered pair of non-empty subset of vertices.
//!                         read more [here](https://en.wikipedia.org/wiki/Hypergraph).
//!
//! * `K-Uniform Hyperedge`: A K-Uniform Hyperedge is a non-empty subset of exactly `K` vertices. All vertices participating in a hyperedge are connected together.
//!                          read more [here](https://en.wikipedia.org/wiki/Hypergraph).
//!
//! * `K-Uniform Directed Hyperedge`: A K-Uniform Directed Hyperedge is a non-empty subset of exactly `K` vertices. It consists of one `tail`(source) that is connected to `K-1` `heads`(destinations).
//!                                   read more [here](https://link.springer.com/book/10.1007%2F978-3-319-00080-0).
